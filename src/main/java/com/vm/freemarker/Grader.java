package com.vm.freemarker;

public class Grader {

	String firstName;
	String lastName;
	
	public Grader(String firstName, String lastName) {
		this.firstName = firstName;
		this.lastName = lastName;
	}
	
	public String getFirstName() {
		return firstName;
	}
	public String getLastName() {
		return lastName;
	}

	
}
